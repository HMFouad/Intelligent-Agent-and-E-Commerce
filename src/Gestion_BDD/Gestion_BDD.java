// Ajout d'un produit
    private void btn_AjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AjouterActionPerformed
try {
    // Vérification si le produit déja existe dans la bdd
    boolean b=false;
    for (int j=0;j<dt.getRowCount();j++){
        if(Integer.parseInt(Txt_Id.getText()) == dt.getValueAt(j, 0)){
            b = true;
            break;
        }
    }
    
    if (b == false){
        St.executeUpdate("Insert into Liste_produits Values('"+Txt_Id.getText()+"','"+Txt_Nom_produit.getText()+"','"+Txt_Quantite.getText()+"','"+Txt_prix_min.getText()+"','"+Txt_prix_max.getText()+"')");
        Afficher_Produits();
        JOptionPane.showMessageDialog(null, "Le produit est ajouté");
    }else {
        JOptionPane.showMessageDialog(null, "L'identifiant existe déja  dans la Bdd");
        Txt_Id.setText("");
    }
    
}catch (Exception e){
    JOptionPane.showMessageDialog(null,"Erreur Ajout d'un produit\n"+e.getMessage());
}
    }//GEN-LAST:event_btn_AjouterActionPerformed

