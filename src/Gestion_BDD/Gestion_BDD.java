    private void Deplacer_Produits(int i){
        try{
            if (i == 0) {
                Btn_Precedent.setEnabled(false);
                Btn_Suivant.setEnabled(true);
            } else if (i == dt.getRowCount()-1) {
                Btn_Precedent.setEnabled(true);
                Btn_Suivant.setEnabled(false);
            }else {
                Btn_Precedent.setEnabled(true);
                Btn_Suivant.setEnabled(true);
            }if (dt.getRowCount()==1 ){
                Btn_Precedent.setEnabled(false);
                Btn_Suivant.setEnabled(false);
            }else if (dt.getRowCount()==0){
                Btn_Precedent.setEnabled(false);
                Btn_Suivant.setEnabled(false);
                Txt_Id.setText("null");
                Txt_Nom_produit.setText("null");
                Txt_Quantite.setText("null");
                Txt_prix_min.setText("null");
                Txt_prix_max.setText("null");
            }
            
            Txt_Id.setText(dt.getValueAt(i, 0).toString());
            Txt_Nom_produit.setText(dt.getValueAt(i, 1).toString());
            Txt_Quantite.setText(dt.getValueAt(i, 2).toString());
            Txt_prix_min.setText(dt.getValueAt(i, 3).toString());
            Txt_prix_max.setText(dt.getValueAt(i, 4).toString());
    }catch (Exception e){
        JOptionPane.showMessageDialog(null, "Erreur déplacement produits\n"+e.getMessage());
    }
    }
    
