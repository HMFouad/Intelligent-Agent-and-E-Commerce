    private void Btn_RechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_RechercherActionPerformed
try {
    dt.setRowCount(0);
    if (jRadioButton1.isSelected()){
        Rs = St.executeQuery("Select * from Liste_produits WHERE Id = "+Txt_Rechercher.getText());
    }else {
        Rs = St.executeQuery("Select * from Liste_produits WHERE Nom_produit = '"+Txt_Rechercher.getText()+"'");
    }
    
    while (Rs.next()){
        Id = Rs.getInt(1);
        Nom_produit = Rs.getString(2);
        Quantite = Rs.getInt(3);
        Prix_min = Rs.getDouble(4);
        Prix_max = Rs.getDouble(5);
            Object[] pdt={Id,Nom_produit,Quantite,Prix_min,Prix_max};
            dt.addRow(pdt);
    }
        if (dt.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Aucun produit");
            Afficher_Produits();
        }else {
            i = 0;
            Deplacer_Produits(i);
        }
    
}catch (Exception e){
    JOptionPane.showMessageDialog(null, "Erreur Boutton Recherche\n"+e.getMessage());
}
    }//GEN-LAST:event_Btn_RechercherActionPerformed

